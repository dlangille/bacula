get_apache_user_group ()
{
	if [ ! -z "$WEB_USER" -a ! -z "$WEB_GROUP" ]
	then
		echo "${WEB_USER}:${WEB_GROUP}"
	elif [ "$SAMPLETYPE" == "rpm-template" ]
