log ()
{
	local errno=$1
	local msg="$2"
	local force_exit=$3
	local type

	if [ $errno -eq 0 ]
	then
		type="INFO"
	elif [ $errno -eq 1 ]
	then
		type="ERROR"
		ERROR=1
	elif [ $errno -eq 2 ]
	then
		type="WARNING"
	else
		type="UNKNOWN"
		ERROR=1
	fi

	echo "[$type]: $msg"

	if [ ! -z "$force_exit" ]
	then
		exit $ERROR
	fi
}

check_user ()
{
	if [ $(id -u) -ne 0 ]
	then
		log 1 "Invalid user. This script should be executed by root." 1
	fi
}

